public class LopHoc
{
    private string malophoc;
    private string tenlophoc;
    private int thanhtien;
    public string MaLopHoc
    {
        get { return malophoc; }
        set { malophoc = value; }
    }
    public string TenLopHoc
    {
        get { return tenlophoc; }
        set { tenlophoc = value; }
    }
    public int ThanhTien
    {
        get { return thanhtien; }
        set { thanhtien = value; }
    }
}

